package com.xing;


public class Orderlist {

    private int lid;
    private int uid;
    private java.util.Date ldate;
    private int lstate;


    public int getLid() {
        return lid;
    }

    public void setLid(int lid) {
        this.lid = lid;
    }


    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }


    public java.util.Date getLdate() {
        return ldate;
    }

    public void setLdate(java.util.Date ldate) {
        this.ldate = ldate;
    }


    public int getLstate() {
        return lstate;
    }

    public void setLstate(int lstate) {
        this.lstate = lstate;
    }

}
