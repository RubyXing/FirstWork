package com.xing;


public class Shopcar {

    private int sid;
    private int uid;
    private int cid;
    private int snum;
    private java.util.Date sdate;


    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }


    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }


    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }


    public int getSnum() {
        return snum;
    }

    public void setSnum(int snum) {
        this.snum = snum;
    }


    public java.util.Date getSdate() {
        return sdate;
    }

    public void setSdate(java.util.Date sdate) {
        this.sdate = sdate;
    }

}
